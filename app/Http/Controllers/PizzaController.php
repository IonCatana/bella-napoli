     * @param  \App\Pizza  $pizza
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Pizza $pizza)
    {
        $request->validate([
            'name' => 'required',
            'description' => 'required',
            'price' => 'required|numeric',
            'img' => 'nullable'
        ]);

        $data = $request->all();
        $pizza->update($data);
        return redirect()->route('pizzas.show', $pizza);
    }

